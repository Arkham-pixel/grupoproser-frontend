/** Estilos react-select — paleta Fénix */
export function getRiesgoSelectStyles(isDark = false) {
  const bg = isDark ? '#1A1A1A' : '#FFFFFF';
  const bgHover = isDark ? '#252525' : '#F9FAFB';
  const bgSelected = isDark ? '#7F1D1D' : '#DC2626';
  const text = isDark ? '#E5E7EB' : '#1F2937';
  const border = isDark ? '#374151' : '#E5E7EB';
  const borderFocus = isDark ? '#EF4444' : '#DC2626';

  return {
    control: (provided, state) => ({
      ...provided,
      minHeight: 40,
      fontSize: '0.875rem',
      backgroundColor: bg,
      borderColor: state.isFocused ? borderFocus : border,
      boxShadow: state.isFocused ? '0 0 0 2px rgba(220, 38, 38, 0.2)' : 'none',
      '&:hover': { borderColor: borderFocus },
    }),
    menuPortal: (provided) => ({ ...provided, zIndex: 9999 }),
    menu: (provided) => ({
      ...provided,
      backgroundColor: bg,
      zIndex: 9999,
      border: `1px solid ${border}`,
    }),
    menuList: (provided) => ({ ...provided, backgroundColor: bg }),
    option: (provided, state) => ({
      ...provided,
      fontSize: '0.875rem',
      backgroundColor: state.isSelected ? bgSelected : state.isFocused ? bgHover : bg,
      color: state.isSelected ? '#FFFFFF' : text,
    }),
    multiValue: (provided) => ({
      ...provided,
      backgroundColor: isDark ? '#374151' : '#FEE2E2',
    }),
    multiValueLabel: (provided) => ({
      ...provided,
      color: isDark ? '#FCA5A5' : '#991B1B',
    }),
    multiValueRemove: (provided) => ({
      ...provided,
      color: isDark ? '#FCA5A5' : '#991B1B',
      '&:hover': { backgroundColor: bgSelected, color: '#FFFFFF' },
    }),
    singleValue: (provided) => ({ ...provided, color: text }),
    input: (provided) => ({ ...provided, color: text }),
    placeholder: (provided) => ({ ...provided, color: isDark ? '#9CA3AF' : '#9CA3AF' }),
  };
}

